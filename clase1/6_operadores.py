

# OPERADORES ARITMÉTICOS

a = 7
b = 5

# Suma (+)
print("Suma:", a + b)

# Resta (-)
print("Resta:", a - b)

# Multiplicación (*)
print("Multiplicación:", a * b)

# División (/). El resultado es un número de punto flotante (float).
print("División:", a / b)

# División entera (//). El resultado es un número entero (int).
print("División entera:", a // b)

# Módulo (%). El resultado es el resto de la división entera.
print("Módulo:", a % b)

# Exponente (**). El resultado es a elevado a la potencia b.
print("Exponente:", a ** b)

